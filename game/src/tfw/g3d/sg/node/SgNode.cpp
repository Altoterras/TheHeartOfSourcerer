//==========================================================================
// SgNode メソッド

/*---------------------------------------------------------------------*//**
	コンストラクタ
**//*---------------------------------------------------------------------*/
SgNode::SgNode()
	: _type(0)
	, _id(0)
	, _prflag(0)
	, _param(0)
{
}

/*---------------------------------------------------------------------*//**
	デストラクタ
**//*---------------------------------------------------------------------*/
SgNode::~SgNode()
{
}

/*---------------------------------------------------------------------*//**
	作成
**//*---------------------------------------------------------------------*/
bool SgNode::create(int type)
{
	// タイプ設定
	_type = type;

	// フラグはデフォルトで表示状態に
	setShow(true);

	return true;
}

/*---------------------------------------------------------------------*//**
	破棄
**//*---------------------------------------------------------------------*/
void SgNode::destroy()
{
}

/*---------------------------------------------------------------------*//**
	コピー
**//*---------------------------------------------------------------------*/
void SgNode::copy(const SgNode* sgnodeSrc)
{
	_type = sgnodeSrc->_type;
	_id = sgnodeSrc->_id;
	_prflag = sgnodeSrc->_prflag;
}

/*---------------------------------------------------------------------*//**
	描画前処理
**//*---------------------------------------------------------------------*/
void SgNode::previse(const RenderCtx* rc)
{
	if(!isShow())
	{
		return;	// 非表示
	}

	previseImplements(rc);
}

/*---------------------------------------------------------------------*//**
	3D 描画
**//*---------------------------------------------------------------------*/
void SgNode::render(const RenderCtx* rc)
{
	if(!isShow())
	{
		return;	// 非表示
	}

	renderImplements(rc);
}

