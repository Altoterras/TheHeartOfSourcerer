// 内部制御

/*---------------------------------------------------------------------*//**
	コンストラクタ
**//*---------------------------------------------------------------------*/
EsStatement::EsStatement()
{
	clear();
}

/*---------------------------------------------------------------------*//**
	コンストラクタ
**//*---------------------------------------------------------------------*/
EsStatement::EsStatement(StatementType type)
{
	clear();
	_type = type;
}

/*---------------------------------------------------------------------*//**
	デストラクタ
**//*---------------------------------------------------------------------*/
EsStatement::~EsStatement()
{
}

/*---------------------------------------------------------------------*//**
	クリア
**//*---------------------------------------------------------------------*/
void EsStatement::clear()
{
	_type = STMTT_NULL;
	_stmtDownScope = 0L;
	_stmtDownEncl = 0L;
	_blockflags = 0;
	_blockid = 0;
	_objBlock = 0L;
	_atomLabel = 0L;
	_bcofsUpdate = -1;
	_bcofsLastBreak = -1;
	_bcofsLastContinue = -1;
}

