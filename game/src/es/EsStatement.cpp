/*---------------------------------------------------------------------*//**
	バイトコードオフセットの先頭値を設定する ⇒ SET_STATEMENT_TOP
**//*---------------------------------------------------------------------*/
void EsStatement::setTopBcOffset(s32 bcofsTop)
{
	_bcofsUpdate = bcofsTop;
	_bcofsLastBreak = -1;
	_bcofsLastContinue = -1;
}

