/*---------------------------------------------------------------------*//**
	クエスト達成チェック
**//*---------------------------------------------------------------------*/
bool QsMod::checkAchievementCustom() const
{
	// デフォルトの実装は、
	// モジュールの実行過程で手動で設定された STAT_ACHVED 状態を維持し、
	// ぞの状態であれば達成済みとみなす
	return _qstaRef->getStat() == QuestArticle::STAT_ACHVED;
}

