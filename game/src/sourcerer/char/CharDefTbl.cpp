/*---------------------------------------------------------------------*//**
	キャラクター定義を取得する
**//*---------------------------------------------------------------------*/
const CharDef* CharDefTbl::getDef(u16 chrdid) const
{
	for(s32 i = 0; i < NUM_CHARDEF_MAX; i++)
	{
		if(_chrdefarr[i].getCharDefId() == chrdid)
		{
			return &_chrdefarr[i];
		}
	}
	return 0L;
}

